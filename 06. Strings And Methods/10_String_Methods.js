console.log("String Methods in Javascript !!");

// count the length of the word
let s = "SuRaj !";
console.log("Length : ", s.length);

// convert into the upper case letters
console.log("Upper Case : ", s.toUpperCase());

// convert into the lower case letters
console.log("Lower Case : ", s.toLowerCase());

// slice
// it will cut the string initial is included and second is excluded
console.log("Slice : ", s.slice(0, 3));

// replace
// it will upted the string
console.log("Replace : ", s.replace(" !", " PaTil"));

// trim
// it will remove the additional/useless space
console.log("Trim : ", s.trim());

// concat
// joins the two string
let st = " Patil"
console.log("Concat : ", s.concat(st));

