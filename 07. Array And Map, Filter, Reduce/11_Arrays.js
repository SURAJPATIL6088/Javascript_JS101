console.log("Arrays in Javascript !!");

// declaring 
let arr = [1, 2, 3, 4, 5, 6];

// print the array
console.log(arr);

// access particular order
console.log("First Element : ", arr[0]);

// get the length of the array
console.log("Length of the array : ", arr.length);

// update the value of an array
arr[0] = 10;
console.log(arr);


// note : array is object
console.log("Array is Type of : ", typeof(arr));
