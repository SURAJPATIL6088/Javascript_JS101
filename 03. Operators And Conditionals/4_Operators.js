console.log("Operators in Javascript !!");


let a = 45;
let b = 4;

// arithmatic operators
console.log("Add : ", a+b);
console.log("Sub : ", a-b);
console.log("Mul : ", a*b);
console.log("Div : ", a/b);

console.log("Mod : ", a%b);

// increment & decrement operators
console.log("Pre Increment : ", ++a);  // 46
console.log("Post Increment : ", a++);  // 47

console.log("Pre Decrement : ", --a);  // 46
console.log("Post Decrement : ", a--);  // 46


// asignment operators
// ==, +=, -=, *=, /=

// boolean operators
let comp1 = 2;
let comp2 = 3;

console.log("Both are Equal : ", comp1 == comp2);
console.log("Both are Equal : ", comp1 != comp2);

// logical operators
let x = 8;
let y = 3;

console.log(x > y && y < x);
console.log(x > y || y < x);
console.log(y == x);

//power 2^5
console.log("2^4 : ", 2 ** 4);